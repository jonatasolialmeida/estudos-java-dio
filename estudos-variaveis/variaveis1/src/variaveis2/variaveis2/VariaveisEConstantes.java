package variaveis2;
public class VariaveisEConstantes {
    public static void main(String[] args) throws Exception {

        // constantes são usadas para não modicar os valores armazenados em memória depois de declarados
        // toda constante escrevemos com CAIXA ALTA

        final double VALOR_DE_PI = 3.14;
        System.out.println(VALOR_DE_PI);
    }
}
